    xhr.onreadystatechange = function () {
      if (xhr.readyState === XMLHttpRequest.DONE && xhr.status === 200) {
        // если запрос успешно выполнен, добавляем новые комментарии в список
        const newComments = xhr.responseText;
        if (newComments.trim()) { // проверяем, есть ли новые комментарии
