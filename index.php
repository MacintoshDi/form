    const formData = new FormData(form);

    // отправляем данные формы на сервер с помощью AJAX
    const xhr = new XMLHttpRequest();
    xhr.open("POST", form.action, true);
    xhr.onreadystatechange = function () {
      if (xhr.readyState === XMLHttpRequest.DONE && xhr.status === 200) {
        // если запрос успешно выполнен, добавляем новый комментарий в список
        const newComment = xhr.responseText;
        if (commentList.children.length === commentsPerPage) { // если на странице уже отображается максимальное количество отзывов
          commentList.removeChild(commentList.lastChild); // удаляем последний отзыв
        }
        commentList.insertAdjacentHTML("beforeend", newComment);
        form.reset(); // сбрасываем форму
      }
    };
    xhr.send(formData);
